package com.senior.services.dto;

public class EmpresaSeniorDto {

    private String dddTel;
    private String emaEmp;
    private String nomEmp;
    private String numEmp;
    private String numIns;
    private String numTel;
    
	public String getDddTel() {
		return dddTel;
	}
	public void setDddTel(String dddTel) {
		this.dddTel = dddTel;
	}
	public String getEmaEmp() {
		return emaEmp;
	}
	public void setEmaEmp(String emaEmp) {
		this.emaEmp = emaEmp;
	}
	public String getNomEmp() {
		return nomEmp;
	}
	public void setNomEmp(String nomEmp) {
		this.nomEmp = nomEmp;
	}
	public String getNumEmp() {
		return numEmp;
	}
	public void setNumEmp(String numEmp) {
		this.numEmp = numEmp;
	}
	public String getNumIns() {
		return numIns;
	}
	public void setNumIns(String numIns) {
		this.numIns = numIns;
	}
	public String getNumTel() {
		return numTel;
	}
	public void setNumTel(String numTel) {
		this.numTel = numTel;
	}

}
